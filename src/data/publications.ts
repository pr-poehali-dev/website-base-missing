export const getPublicationById = (id: number): Publication | undefined => {
  return publications.find(pub => pub.id === id);
};

export const getNextPublication = (id: number): Publication | undefined => {
  const currentIndex = publications.findIndex(pub => pub.id === id);
  if (currentIndex === -1 || currentIndex === publications.length - 1) return undefined;
  return publications[currentIndex + 1];
};

export const getPreviousPublication = (id: number): Publication | undefined => {
  const currentIndex = publications.findIndex(pub => pub.id === id);
  if (currentIndex <= 0) return undefined;
  return publications[currentIndex - 1];
};